def add_info(img:np.ndarray, infodict:dict, margin:int=15, color:tuple=(0, 0, 0), start_pos:str="TL"):

    if start_pos == "TL":
        start_point = (10, 15)
    elif start_pos == "TR":
        start_point = (img.shape[1] - 30, 15)
    elif start_pos == "BL":
        start_point = (10, img.shape[0] - len(infodict.keys()) * margin - 10)
    else:
        start_point = (img.shape[1] - 30, img.shape[0] - len(infodict.keys()) * margin - 10)
    i = 0
    for key, value in infodict.items():
        new_point = (start_point[0], start_point[1] + i * margin)
        cv2.putText(img, key + " :" + str(value), new_point, cv2.FONT_HERSHEY_PLAIN, 1, color, 2,
                cv2.LINE_AA)
        i += 1